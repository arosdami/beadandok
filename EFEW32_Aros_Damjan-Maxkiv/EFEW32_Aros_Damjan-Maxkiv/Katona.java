package org.example;

public class Katona {
    private String nev;
    private int fizetes;

    public Katona(String nev, int fizetes) {
        this.nev = nev;
        this.fizetes = fizetes;
    }

    public String getNev() {
        return nev;
    }

    public int getFizetes() {
        return fizetes;
    }
}
